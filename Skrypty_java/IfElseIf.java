public class IfElseIf {

    public static void main(String[] args) {

        int liczba = 0;

        if (liczba == 0) {
            System.out.println("Liczba jest równa 0");
        }

        else if (liczba < 0) {
            System.out.println("Liczba jest mniejsza od 0");
        }

        else {
            System.out.println("Liczba jst większa od 0");
        }
    }
}

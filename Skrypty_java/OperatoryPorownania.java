public class OperatoryPorownania { // sprawdzają wartości i zwracają true / false

    public static void main(String[] args) {
        int pierwszaLiczba = 4;
        int drugaLiczba = 6;

        boolean rezultat = pierwszaLiczba > drugaLiczba;

        System.out.println(rezultat);

        System.out.println(pierwszaLiczba > drugaLiczba);
        System.out.println(pierwszaLiczba < drugaLiczba);
        System.out.println(pierwszaLiczba >= drugaLiczba);
        System.out.println(pierwszaLiczba <= drugaLiczba);
        System.out.println(pierwszaLiczba == drugaLiczba); // równe
        System.out.println(pierwszaLiczba != drugaLiczba); // nie równe
    }
}
